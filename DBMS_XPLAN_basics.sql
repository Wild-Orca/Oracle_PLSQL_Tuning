Part 1: Generating Execution Plans
***1.1 EXPLAIN PLAN FOR
This command generates an execution plan for the subsequent SQL statement. 
EXPLAIN PLAN FOR
SELECT 1
  FROM DUAL;

1.2 SELECT * FROM TABLE(DBMS_XPLAN.DISPLAY());
This displays the execution plan generated by the previous EXPLAIN PLAN FOR statement.

1.3 SELECT * FROM TABLE(DBMS_XPLAN.DISPLAY(NULL,NULL,'BASIC'));
Displays a basic version of the execution plan.

1.4 SELECT * FROM TABLE(DBMS_XPLAN.DISPLAY(NULL,NULL,'TYPICAL'));
Displays a more typical version of the execution plan.

1.5 SELECT * FROM TABLE(DBMS_XPLAN.DISPLAY(NULL,NULL,'ALL'));
Displays all available information in the execution plan.

***Part 2: Gathering Query Statistics (Obtaining QUERY_ID and CHILD_NUMBER)
2.1 SELECT /*+ GATHER_PLAN_STATISTICS */ 
           1 
      FROM DUAL;
This command hints the database to gather statistics for the execution plan of the subsequent SQL statement.

2.2 SELECT * FROM TABLE(DBMS_XPLAN.DISPLAY_CURSOR);
Displays the execution plan along with statistics gathered from the previous query.

***Part 3: Verifying Execution Plan
3.1 SELECT * FROM TABLE(DBMS_XPLAN.DISPLAY_CURSOR('QUERY_ID', CHILD_NUMBER, 'ALL'));
Displays the execution plan and statistics for a specific query identified by its QUERY_ID and CHILD_NUMBER.

3.2 SELECT * FROM TABLE(DBMS_XPLAN.DISPLAY_CURSOR('QUERY_ID', CHILD_NUMBER, 'ALL IOSTATS'));
Displays execution plan and I/O statistics.

3.3 SELECT * FROM TABLE(DBMS_XPLAN.DISPLAY_CURSOR('QUERY_ID', CHILD_NUMBER, 'ALL MEMSTATS'));
Displays execution plan and memory-related statistics.

3.4 SELECT * FROM TABLE(DBMS_XPLAN.DISPLAY_CURSOR('QUERY_ID', CHILD_NUMBER, 'ALL ALLSTATS'));
Displays execution plan and all available statistics.

3.5 SELECT * FROM TABLE(DBMS_XPLAN.DISPLAY_CURSOR(NULL, NULL, 'ALL ALLSTATS LAST'));
Displays the execution plan and all available statistics for the most recently executed query.

These commands are useful for analyzing and optimizing the performance of SQL queries in an Oracle 12c database by examining their execution plans and associated statistics.